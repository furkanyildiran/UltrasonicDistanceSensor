/* USER CODE BEGIN PV */
typedef struct{
  	uint16_t reserved1:3;
  	uint16_t data:4;
  	uint16_t display_on:3;
  	uint16_t reserved2:6;
}segment_t;
typedef union{
	segment_t segmet;
  	uint16_t reg_val;
}segment_u;
typedef enum{
	DISPLAY1_ON=1,
	DISPLAY2_ON=2,
	DISPLAY3_ON=4,
}display_status_t;
segment_u segment;
uint16_t display_val;
/* USER CODE END PV */

/* Private function prototypes -----------------------------------------------*/
/* USER CODE BEGIN PFP */
void set_display(uint16_t val){
	display_val  = val;
}
/* USER CODE END PFP */

/* Private user code ---------------------------------------------------------*/
/* USER CODE BEGIN 0 */
void execute_displays(uint16_t num){
	static uint8_t counter;
	if(counter==0){
		segment.segmet.display_on = DISPLAY3_ON;
		segment.segmet.data=num%10;
	}
	else if(counter==1){
		segment.segmet.display_on = DISPLAY2_ON;
		segment.segmet.data=(num/10)%10;
	}
	else if(counter==2){
		segment.segmet.display_on = DISPLAY1_ON;
		segment.segmet.data=num/100;
	}
	counter++;
	if(counter==3)counter=0;
	GPIOB->ODR = segment.reg_val;
}
/* USER CODE END 0 */

